    public void caldular_descuento() {
        double n1, n2, n3, n4, nt;

        String num1X, num2X, num3X;
        String resultadoX, resultadoY;

        num1X = txtMonto.getText();
        num2X = jSpDescuento.getValue().toString();

        n1 = Double.parseDouble(num1X);
        n2 = Double.parseDouble(num2X);

        n3 = (n2 / 100);
        n4 = (n1 * n3);
        nt = (n1 - n4);

        resultadoX = String.valueOf(nt);
        resultadoY = String.valueOf(n4);
        txtMontoFinal.setText(resultadoX);
        //Evento para mostrar el valor del descuento al cambiar el spinner
        lblDiferencia.setText("Ahorro = " + resultadoY);
        txtDiferencia.setText(resultadoY);
        
        txtConcepto.grabFocus();
    }
    
    public void guardar_descuento() {
        if (jSpDescuento.getValue().toString().equals("0")) {
            JOptionPane.showMessageDialog(null, "Seleccione % de Descuento", "Completar InformaciÃ³n", 1);
            jSpDescuento.grabFocus();
        } else if(txtConcepto.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Falta Detalles del Descuento", "Completar InformaciÃ³n", 1);
            txtConcepto.grabFocus();
            
        } else {
            String tCliente, tFecha, tSubtotal, tPorciento, tDescuento, tTotal, tConcepto;
            String sql = "";
            tCliente = txtCliente.getText();
            tFecha = txtFecha.getText();
            tSubtotal = txtMonto.getText();
            tPorciento = jSpDescuento.getValue().toString();
            tDescuento = txtDiferencia.getText();
            tTotal = txtMontoFinal.getText();
            tConcepto = txtConcepto.getText();
            
            sql = "INSERT INTO descuentos(cliente, fecha, sub_total, porciento, descuento, total, concepto) VALUES(?,?,?,?,?,?,?)";
            try {
                PreparedStatement pst = cn.prepareCall(sql);

                pst.setString(1, tCliente);
                pst.setString(2, tFecha);
                pst.setString(3, tSubtotal);
                pst.setString(4, tPorciento);
                pst.setString(5, tDescuento);
                pst.setString(6, tTotal);
                pst.setString(7, tConcepto);
                int n = pst.executeUpdate();
                if (n > 0) {
                    JOptionPane.showMessageDialog(null, "* DESCUENTO REGISTRADO *", "OperaciÃ³n Exitosa", 1);
                }
            } catch (SQLException ex) {
                Logger.getLogger(procesar_pago.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            String n_montofinal = txtMontoFinal.getText();
            procesar_pago.txt_total.setText(n_montofinal);
            String n_descuento = txtDiferencia.getText();
            procesar_pago.txt_descuento.setText(n_descuento);
    
            this.setVisible(false);
            
        }
    }
