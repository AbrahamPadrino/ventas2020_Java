    private void btn_modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_modificarActionPerformed
        String regist;
        String sql = ("");
        String exp = txt_nombre_user.getText();

        boolean existe = false; //variable bandera para comprobar si NO existe el expediente en la BD

        try {
            sql = "SELECT * FROM usuarios WHERE nombre_usuario = '" + exp + "'";

            ResultSet rs = sent.executeQuery(sql);

            while (rs.next()) {
                existe = true;
                if (existe == true) {
                    regist = rs.getString("nombre_usuario");
                    if (exp.equals(regist)) {
                        JOptionPane.showMessageDialog(null, "" + regist + "  YA EXISTE", "Mensaje", JOptionPane.ERROR_MESSAGE);
                        cancelar();
                        txt_nombre_user.grabFocus();

                    }
                }
            } //fin while res.next
            if (existe == false) {
                if (txt_nombre_user.getText().equals("")) {
                    JOptionPane.showMessageDialog(null, "Ha Dejado el Campo 'Nombre' Vacio", "aviso", JOptionPane.ERROR_MESSAGE);
                } else if (txt_clave_user.getText().equals("")) {
                    JOptionPane.showMessageDialog(null, "Ha Dejado el Campo 'Clave' Vacio", "aviso", JOptionPane.ERROR_MESSAGE);
                } else if (jComboBox1.getSelectedItem().equals("Seleccione...")) {
                    JOptionPane.showMessageDialog(null, "Ha Dejado el Campo 'Tipo' Vacio", "aviso", JOptionPane.ERROR_MESSAGE);
                } else {

                    String nom, cla, tip;

                    nom = txt_nombre_user.getText();
                    cla = txt_clave_user.getText();
                    tip = (String) jComboBox1.getSelectedItem();

                    try {
                        sql = "Update usuarios set nombre_usuario=?, clave_usuario=?, tipo_usuario=?"
                                + "where id=?";
                        int fila = jTableUsuarios.getSelectedRow();
                        String dao = (String) jTableUsuarios.getValueAt(fila, 0);
                        PreparedStatement pst = cn.prepareStatement(sql);
                        pst.setString(1, nom);
                        pst.setString(2, cla);
                        pst.setString(3, tip);

                        pst.setString(4, dao);

                        int n = pst.executeUpdate();
                        if (n > 0) {
                            JOptionPane.showMessageDialog(null, "Registro Modificado");

                        }
                        CargarUsuarios("");
                        cancelar();

                    } catch (HeadlessException | SQLException e) {

                    }
                }
            }
            rs.close();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }


    }//GEN-LAST:event_btn_modificarActionPerformed

