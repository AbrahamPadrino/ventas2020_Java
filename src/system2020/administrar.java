    void CargarUsuarios(String valor) {
        try {
            String[] titulos = {"ID", "Nombre", "Clave", "Tipo"};
            String[] registros = new String[5];
            String sql = "SELECT * FROM usuarios where id LIKE '%" + valor + "%' ";
            model = new DefaultTableModel(null, titulos);
            sent = cn.createStatement();
            ResultSet rs = sent.executeQuery(sql);
            while (rs.next()) {
                registros[0] = rs.getString("id");
                registros[1] = rs.getString("nombre_usuario");
                registros[2] = rs.getString("clave_usuario");
                registros[3] = rs.getString("tipo_usuario");

                model.addRow(registros);
            }
            jTableUsuarios.setModel(model);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }

    public void guardar_usuario() {
        if (txt_nombre_user.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Falta *Nombre de Usuario*", "Completar Información", 1);
            txt_nombre_user.grabFocus();
        } else if (txt_clave_user.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Falta *Clave de Usuario*", "Completar Información", 1);
            txt_clave_user.grabFocus();
        } else if (jComboBox1.getSelectedItem().equals("Seleccione")) {
            JOptionPane.showMessageDialog(null, "Falta *Tipo de Usuario*", "Completar Información", 1);
            jComboBox1.grabFocus();
        } else {
            String tNombre, tClave, tTipo;
            String sql = "";
            tNombre = txt_nombre_user.getText();
            tClave = txt_clave_user.getText();
            tTipo = jComboBox1.getActionCommand();

            sql = "INSERT INTO usuarios(nombre_usuario, clave_usuario, tipo_usuario) VALUES(?,?,?)";
            try {
                PreparedStatement pst = cn.prepareCall(sql);

                pst.setString(1, tNombre);
                pst.setString(2, tClave);
                pst.setString(3, jComboBox1.getSelectedItem().toString());

                int n = pst.executeUpdate();
                if (n > 0) {
                    JOptionPane.showMessageDialog(null, "* Usuario Registrado *", "Operación Exitosa", 1);
                }
            } catch (SQLException ex) {
                Logger.getLogger(procesar_pago.class.getName()).log(Level.SEVERE, null, ex);
            }
            CargarUsuarios("");

            txt_nombre_user.setEditable(false);
            txt_clave_user.setEditable(false);

            btn_editar.setVisible(true);
            btn_modificar.setVisible(false);

            btn_nuevo.setEnabled(true);
            btn_guardar.setEnabled(true);
            btn_editar.setEnabled(true);
            btn_eliminar.setEnabled(true);

            txt_nombre_user.setText("");
            txt_clave_user.setText("");
            jComboBox1.setSelectedItem("Seleccione");

        }
    }

    public void registrar_usuario() {
        String regist;
        String sql = ("");
        String exp = txt_nombre_user.getText();

        boolean existe = false; //variable bandera para comprobar si NO existe el expediente en la BD

        try {
            sql = "SELECT * FROM usuarios WHERE nombre_usuario = '" + exp + "'";

            ResultSet rs = sent.executeQuery(sql);

            while (rs.next()) {
                existe = true;
                if (existe == true) {
                    regist = rs.getString("nombre_usuario");
                    if (exp.equals(regist)) {
                        JOptionPane.showMessageDialog(null, "El Usuario: " + regist + " Ya Está Registrado", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
                        txt_nombre_user.setText("");
                        txt_nombre_user.grabFocus();
                    }
                }
            }//fin while res.next
            if (existe == false) {
                guardar_usuario();
            }
            rs.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

